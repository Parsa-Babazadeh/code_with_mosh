# Addition operator + :
print(10 + 3)

# Subtraction - :
print(10 - 3)

# Multiplication * :
print(10 * 3)

# Division "/" & "//" :
# We have two kinds of Division.
# Here's one with forward slash / :
print(10 / 3)  # we get a floating point number. Answer: 3.3333333333333335

# But we also have another division operator for getting an integer; If we add another slash // :
print(10 // 3)  # we get an integer. Answer: 3

# Modulus % :
# This returns the remainder of the division.
print(10 % 3)

# Exponent:
print(10 ** 3)

